package com.edushift.modules.evaluations.entity;

import com.edushift.modules.academic.unit.entity.Unit;
import com.edushift.modules.sessions.learning.entity.LearningSession;
import com.edushift.modules.teachers.assignments.entity.TeacherAssignment;
import com.edushift.shared.domain.TenantAwareEntity;
import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.EnumType;
import jakarta.persistence.Enumerated;
import jakarta.persistence.FetchType;
import jakarta.persistence.ForeignKey;
import jakarta.persistence.Index;
import jakarta.persistence.JoinColumn;
import jakarta.persistence.ManyToOne;
import jakarta.persistence.Table;
import jakarta.persistence.UniqueConstraint;
import java.math.BigDecimal;
import java.time.Instant;
import java.time.LocalDate;
import java.util.UUID;
import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;
import lombok.ToString;
import org.hibernate.annotations.SQLDelete;

/**
 * Graded item authored by a teacher against a
 * {@link TeacherAssignment} (Sprint 5B / BE-5B.1).
 *
 * <p>An {@code Evaluation} carries its own {@link EvaluationScale}
 * (numeric or literal) and is governed by the
 * {@link EvaluationStatus} lifecycle. It can be anchored (for
 * pedagogical traceability) to a {@link Unit} (BE-5A.1) or a
 * {@link LearningSession} (BE-5A.4) — both optional.</p>
 *
 * <h3>Identity</h3>
 * {@code publicUuid} is the externally-exposed id (used in URLs and
 * GradeRecord FKs). Internally the table uses a stable {@code UUID v7}
 * generated by {@code UuidV7Generator} (see V25 migration comments).
 *
 * <h3>Concurrency</h3>
 * Lifecycle transitions (publish/close) are race-safe: the service
 * loads the row, applies the state machine, and re-asserts the
 * status from the DB. CLOSED is terminal (no further writes).
 *
 * <h3>Multi-tenant</h3>
 * Tenant isolation is enforced by Hibernate's {@code @TenantId}
 * filter on {@link TenantAwareEntity}. A second row of protection
 * comes from the {@code uk_evaluations_tenant_assignment_name_ci}
 * unique index.
 */
@Entity
@Table(
		name = "evaluations",
		schema = "edushift",
		uniqueConstraints = {
				@UniqueConstraint(name = "uk_evaluations_public_uuid",
						columnNames = "public_uuid")
		},
		indexes = {
				@Index(name = "idx_evaluations_tenant_assignment_date",
						columnList = "tenant_id, teacher_assignment_id, scheduled_date"),
				@Index(name = "idx_evaluations_tenant_status_date",
						columnList = "tenant_id, status, scheduled_date"),
				@Index(name = "idx_evaluations_tenant_unit",
						columnList = "tenant_id, unit_id"),
				@Index(name = "idx_evaluations_tenant_session",
						columnList = "tenant_id, learning_session_id")
		}
)
@Getter
@Setter
@NoArgsConstructor
@ToString(callSuper = true,
		of = {"publicUuid", "name", "kind", "scale", "status", "scheduledDate"})
@SQLDelete(sql = "UPDATE edushift.evaluations "
		+ "SET deleted = true, deleted_at = NOW(), updated_at = NOW() "
		+ "WHERE id = ?")
public class Evaluation extends TenantAwareEntity {

	@Column(name = "public_uuid", nullable = false, updatable = false,
			unique = true, columnDefinition = "uuid")
	private UUID publicUuid;

	@ManyToOne(fetch = FetchType.LAZY, optional = false)
	@JoinColumn(name = "teacher_assignment_id", nullable = false,
			columnDefinition = "uuid",
			foreignKey = @ForeignKey(name = "fk_evaluations_assignment"))
	private TeacherAssignment teacherAssignment;

	@ManyToOne(fetch = FetchType.LAZY)
	@JoinColumn(name = "unit_id", columnDefinition = "uuid",
			foreignKey = @ForeignKey(name = "fk_evaluations_unit"))
	private Unit unit;

	@ManyToOne(fetch = FetchType.LAZY)
	@JoinColumn(name = "learning_session_id", columnDefinition = "uuid",
			foreignKey = @ForeignKey(name = "fk_evaluations_learning_session"))
	private LearningSession learningSession;

	@Enumerated(EnumType.STRING)
	@Column(name = "kind", nullable = false, length = 16)
	private EvaluationKind kind;

	@Column(name = "name", nullable = false, length = 200)
	private String name;

	@Column(name = "description", columnDefinition = "text")
	private String description;

	@Column(name = "weight", nullable = false, precision = 5, scale = 2)
	private BigDecimal weight;

	@Column(name = "scheduled_date", nullable = false)
	private LocalDate scheduledDate;

	@Column(name = "due_date")
	private LocalDate dueDate;

	@Enumerated(EnumType.STRING)
	@Column(name = "scale", nullable = false, length = 24)
	private EvaluationScale scale;

	@Enumerated(EnumType.STRING)
	@Column(name = "status", nullable = false, length = 16)
	private EvaluationStatus status = EvaluationStatus.DRAFT;

	@Column(name = "published_at")
	private Instant publishedAt;

	@Column(name = "closed_at")
	private Instant closedAt;

	@Column(name = "is_active", nullable = false)
	private Boolean isActive = Boolean.TRUE;

	@Column(name = "deleted_at")
	private Instant deletedAt;
}
